package extensions;

/**
 *
 * @author silva
 */
public class FloatExtension {
    public static boolean tryParseFloat(String value) {
        try {
            Float.parseFloat(value);
            return true;
        } catch (NumberFormatException e) {
            return false;
        }
    }
}
